def lime_to_vector(exp, n_features):
    vec = np.zeros(n_features)
    for feature_idx, weight in exp.local_exp[1]:
        vec[feature_idx] = weight

    return vec

# make vectors
vec1 = lime_to_vector(lime_exp_1, len(X.columns))
vec2 = lime_to_vector(lime_exp_2, len(X.columns))

# stability score
lime_diff = np.mean(np.abs(vec1 - vec2))

print("LIME explanation 1:", vec1)
print("LIME explanation 2:", vec2)
print("LIME stability:", lime_diff)

