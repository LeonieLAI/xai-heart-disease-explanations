def eli5_to_vector(exp, feature_names):

    vec = np.zeros(len(feature_names))

    weights = exp.targets[0].feature_weights.pos + exp.targets[0].feature_weights.neg

    for w in weights:
        if w.feature in feature_names:
            idx = feature_names.index(w.feature)
            vec[idx] = w.weight

    return vec

vec1 = eli5_to_vector(exp_eli5_1, X.columns.tolist())
